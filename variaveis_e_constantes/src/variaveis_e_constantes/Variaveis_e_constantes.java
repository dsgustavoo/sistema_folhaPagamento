package variaveis_e_constantes;

public class Variaveis_e_constantes {
    public static void main(String[] args) {
    //para se declarar uma constante é necessario colocar FINAL <tipo de dado> <nome> = <valor>;
    
    final float PI = 3.146f;
    final double E = 1.234e2;
    String nome = "João Pedro";
    int lote = 75825;
    boolean aprovado = true;
    
    System.out.println(PI);
    System.out.println(E);
    System.out.println(nome);
    System.out.println(lote);
    System.out.println(aprovado);
    }
    
}
